    FOREIGN KEY (GLACCOOUNTID)         REFERENCES PLANNING.GLACCOUNT (GLACCOOUNTID),
    FOREIGN KEY (COSTCENTERID)         REFERENCES PLANNING.COSTCENTER (COSTCENTERID),
    FOREIGN KEY (FISCALPERIODID)       REFERENCES PLANNING.FISCALPERIOD (FISCALPERIODID),
    FOREIGN KEY (ALLOCATIONSOURCELINEID) REFERENCES PLANNING.BUDGETLINEITEM (BUDGETLINEITEMID)
