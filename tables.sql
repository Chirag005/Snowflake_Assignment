ALTER TABLE PLANNING.BUDGETLINEITEM ADD CONSTRAINT UQ_BUDGETLINEITEM_NATURALKEY
    UNIQUE (BUDGETHEADERID, GLACCOOUNTID, COSTCENTERID, FISCALPERIODID);

CREATE TABLE IF NOT EXISTS PLANNING.CONSOLIDATIONJOURNAL (
    JOURNALID               BIGINT          NOT NULL AUTOINCREMENT PRIMARY KEY,
    JOURNALNUMBER           VARCHAR(50)     NOT NULL,
    JOURNALTYPE             VARCHAR(30)     NOT NULL,
    BUDGETHEADERID          INTEGER         NOT NULL,
    FISCALPERIODID          INTEGER         NOT NULL,
    POSTINGDATE             DATE            NOT NULL,
    DESCRIPTION             VARCHAR(500)    NULL,
    STATUSCODE              VARCHAR(20)     NOT NULL DEFAULT 'DRAFT',
    SUPPORTINGDOCUMENTREF   VARCHAR(500)    NULL,
    JOURNALGUID             VARCHAR(36)     NOT NULL DEFAULT UUID_STRING(),
